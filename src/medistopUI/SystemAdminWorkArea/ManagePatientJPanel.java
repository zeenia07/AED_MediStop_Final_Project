    private void populatePatinetTable() {
        DefaultTableModel model = (DefaultTableModel) tblPatient.getModel();
        model.setRowCount(0);
        for (PatientData network : ecosystem.getPatientDir().getPatientDirectory()) {
            Object[] row = new Object[1];
            row[0] = network;
            model.addRow(row);
        }
    }

