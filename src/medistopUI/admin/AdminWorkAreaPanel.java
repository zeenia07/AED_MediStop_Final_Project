    //Table to see employee details
    private void populateEmployeeTable(Organisation organization){
        DefaultTableModel model = (DefaultTableModel) tblEmployee.getModel();
        
        model.setRowCount(0);
        
        for (Employee employee : organization.getEmployeeDirectory().getEmployeeDirectory())
        {
            Object[] row = new Object[2];
            row[0] = employee.getEmpId();
            row[1] = employee.getEmployeeName();
            model.addRow(row);
        }
    }
   
